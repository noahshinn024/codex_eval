def format_str_complex(str1, str2):
  """
  Reverses, and deletes every other letter from a combination of two strings and
  returns a string of all of the vowels in reverse order
  """
  
  v1 = str1[::-1]
  v2 = str2[::-1]
  v3 = v1[1::2] + v2[0::2]
  return ''.join(reversed(v3))


